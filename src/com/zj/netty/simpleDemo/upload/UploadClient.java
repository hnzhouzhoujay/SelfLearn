	/*
	 * 创建一个get请求，可以带参数也可不带参数
	 */
	private void createRequest(String uri,String host,int port,Map<String,String> param){
		ChannelFuture future=b.connect(host, port);
		try {
			Channel ch=future.sync().channel();
			HttpRequest request=null;
			if(param!=null){
				QueryStringEncoder encoder=new QueryStringEncoder(uri);
				for(String name:param.keySet())
					encoder.addParam(name, param.get(name));
				request=new DefaultHttpRequest(HttpVersion.HTTP_1_1, HttpMethod.GET, encoder.toUri().toASCIIString());
			}else{
				request=new DefaultHttpRequest(HttpVersion.HTTP_1_1, HttpMethod.GET, uri);
			}
			HttpHeaders header=request.headers();
			setDefaultHeaders(header, host);
			ch.write(request);
			ch.flush();
			ch.closeFuture().sync();
		} catch (InterruptedException e) {
			e.printStackTrace();
		} catch (URISyntaxException e) {
			e.printStackTrace();
		}
	}
	/**
	 * 创建multipart/form-data复杂的post请求，能传文件
	 * @param uri
	 * @param host
	 * @param port
	 * @param file
	 * @param param
	 */
	private void createFileUploadPost(URI uri,String host,int port,File file,Map<String,String> param){
		ChannelFuture future=b.connect(new InetSocketAddress(host, port));
		try {
			Channel ch=future.sync().channel();
			
			HttpRequest request=new DefaultHttpRequest(HttpVersion.HTTP_1_1, HttpMethod.POST, uri.toASCIIString());
			HttpHeaders header=request.headers();
			setDefaultHeaders(header, host);
			/* 第三个参数决定了content-type:multipart/form-data,这种才能传文件*/
			HttpPostRequestEncoder postEncoder=new HttpPostRequestEncoder(factory,request,true);
			/*   很奇怪param中不能放入静态变量,如要addBodyAttribute加入静态变量，不能写在循环中
			 *    否则很隐秘的发给服务器的请求不对，将收不到文件      */
			for(Entry<String,String> entry:param.entrySet()){
				postEncoder.addBodyAttribute(entry.getKey(), entry.getValue());
			}
			postEncoder.addBodyAttribute("longtxt", textAreaLong);
			/* 在这种请求中发送 type为HttpDataType.FileUpload的   httpdata ，上面发送的属性为HttpDataType.Attribute*/
			postEncoder.addBodyFileUpload("myfile", new File("upload.txt"), "application/x-zip-compressed", false);
			postEncoder.finalizeRequest();
			ch.write(request);
			/*        如果是chunked ,要写入chunked内容通过ChunkerWriter去写*/
			if(postEncoder.isChunked()){
				ch.write(postEncoder);
			}
			ch.flush();
			postEncoder.cleanFiles();
			/*  等待关闭  */
			ch.closeFuture().sync();
		} catch (InterruptedException e) {
			e.printStackTrace();
		}catch (ErrorDataEncoderException e) {
			e.printStackTrace();
		}finally{
			group.shutdownGracefully();
		}
	
		
	}
	private void setDefaultHeaders(HttpHeaders header,String host){
		header.set(HttpHeaderNames.HOST, host);
		header.set(HttpHeaderNames.CONNECTION, HttpHeaderValues.CLOSE);
		header.set(HttpHeaderNames.ACCEPT, "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8");
		header.set(HttpHeaderNames.ACCEPT_LANGUAGE,"fr");
		header.set(HttpHeaderNames.ACCEPT_ENCODING,HttpHeaderValues.GZIP+","+HttpHeaderValues.DEFLATE);
		header.set(HttpHeaderNames.ACCEPT_CHARSET,"ISO-8859-1,utf-8;q=0.7,*;q=0.7");
		header.set(HttpHeaderNames.USER_AGENT,"Mozilla/4.04[en](Win95;I;Nav)");
		header.set(HttpHeaderNames.REFERER, "http://127.0.0.1:8080/");
	}
